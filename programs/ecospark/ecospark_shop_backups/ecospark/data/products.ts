export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getProductsByCategory(category: string): Product[] {
  return products.filter(p => p.category === category);
}

export function getBestsellerProducts(): Product[] {
  return products.filter(p => p.isBestseller);
}

export function getHeroProducts(): Product[] {
  return products.filter(p => p.isHeroProduct);
}

export function getRelatedProducts(product: Product, limit: number = 3): Product[] {
  return products
    .filter(p => p.id !== product.id && (p.category === product.category || p.tags.some(tag => product.tags.includes(tag))))
    .slice(0, limit);
}


